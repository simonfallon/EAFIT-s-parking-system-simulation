struct user{
  int id, id_f, id_f_real, id_p, pos_f, arr_t, arr_t_real;
  int tran_t, num_tran, ent_t, exp_ent_t, ex_t, exp_ex_t, ex_t_real;
  bool app, emp;

  user(int id, int id_f, int id_p, int arr_t, bool app, bool emp):
    id(id), id_f(id_f), id_f_real(0), id_p(id_p), pos_f(0), arr_t(arr_t),
    arr_t_real(0),tran_t(0), num_tran(0), ent_t(0), exp_ent_t(0),
    exp_ex_t(0), ex_t(0), ex_t_real(0), app(app), emp(emp) {}

  user() {}

  bool operator < (user u){
    if(ex_t != u.ex_t){
      return ex_t < u.ex_t;
    } else if(exp_ex_t != u.exp_ex_t){
      return exp_ex_t < u.exp_ex_t;
    } else{
      return id < u.id;
    }
  }
};

